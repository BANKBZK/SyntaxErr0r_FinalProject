using UnityEngine;
using UnityEngine.UI;

public class BuffUIManager : MonoBehaviour
{
    [Header("Dependencies")]
    public SkillBook skillBook;      // ??? SkillBook ??????? Inspector
    public BuffUIAssets buffAssets;  // ??? Database ?????????????????

    [Header("UI Elements")]
    public Image buffImage;

    float timer;
    bool isShowing = false;

    void OnEnable()
    {
        // ??? SkillBook ????????????? ???????????????? OnSkillUsed
        if (skillBook != null)
            skillBook.OnSkillActivated += OnSkillUsed;
    }

    void OnDisable()
    {
        // ??????????????????????????/?????????? (??????? Error)
        if (skillBook != null)
            skillBook.OnSkillActivated -= OnSkillUsed;
    }

    void Start()
    {
        buffImage.gameObject.SetActive(false);
    }

    // ???????????????????????????????? SkillBook ?????? Event
    void OnSkillUsed(Skill skill)
    {
        // ??????????????????????????????? (??? skill.timer <= 0 ??????????????? ??????????????)
        if (skill.timer > 0)
        {
            // ??????????? Assets ??????????????
            Sprite s = buffAssets.GetSprite(skill.skillName);

            if (s != null)
            {
                ShowBuff(skill.timer, s);
            }
        }
    }

    public void ShowBuff(float duration, Sprite sprite)
    {
        buffImage.sprite = sprite;
        buffImage.gameObject.SetActive(true);
        timer = duration;
        isShowing = true;
    }

    void Update()
    {
        if (!isShowing) return;

        timer -= Time.deltaTime;

        if (timer <= 0)
        {
            buffImage.gameObject.SetActive(false);
            isShowing = false;
        }
    }
}